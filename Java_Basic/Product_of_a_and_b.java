import java.util.*;
public class Product_of_a_and_b{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int a, b, product;
        
        a = sc.nextInt();
        b = sc.nextInt();
        
        product = a*b;
        
        System.out.println("Product of a & b = "+product);
    }
}
