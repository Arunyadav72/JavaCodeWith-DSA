public class Data_Type{
    public static void main(String args[]){
        byte b = 8;
        System.out.println(b);
        
        char ch = 'a';
        System.out.println(ch);
        
        boolean var = false;
        System.out.println(var);
        
        float price = 10.5f;
        int number = 25;
        System.out.println(price);
        System.out.println(number);
    }
}
