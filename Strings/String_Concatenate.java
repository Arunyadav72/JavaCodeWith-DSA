/*
    Syntax..
       Str = str1 + str.
       Here + concatenate two string.
*/
public class String_Concatenate{
    
    public static void main(String args[]){
        String  first_name = "Arun";
        String second_name = "Yadav";
        
        //Here Concatenate Two String.
        String Full_name = first_name+" "+second_name;
        
        //display / print.
        System.out.println("First name is : "+first_name);
        System.out.println("Second name is : "+second_name);
        System.out.println("Full name is : "+Full_name);
    }
}