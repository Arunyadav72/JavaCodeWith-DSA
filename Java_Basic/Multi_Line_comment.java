public class Multi_Line_comment{
    public static void main(String args[]){
        int a = 30;
        double f = 43.3;
        char ch = 'A';
        String name = "Sharadha Khapara";
        
        /*
        
        Variable 'a' is Int(Integer(0-9)) data type.
        Variable 'f' is double(point value(0.0)) data type.
        Variable 'ch' is char(Character(A-Z, a-z)) data type.
        Variable 'nume' is String(long character) data type.
        
        */
        
        System.out.println(a);
        System.out.println(f);
        System.out.println(ch);
        System.out.println(name);
    }
}
